#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char i;

	i = '0';
	if (n == -2147483648)
	{
		write(fd, "-", 1);
		write(fd, "2", 1);
		n = 147483648;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	i = n % 10 + '0';
	write(fd, &i, 1);
}
